import { PDFPage } from "@/types/pdf2image";
import { canvasToJPG } from "./pdf2imageUtils";

/**
 * 创建PPTX文件结构
 */
export async function createPPTXStructure(zip: any, pages: PDFPage[]): Promise<void> {
  // 添加基础文件结构
  zip.file('[Content_Types].xml', generateContentTypesXML(pages.length));
  
  // 添加_rels目录
  const relsFolder = zip.folder('_rels');
  relsFolder.file('.rels', generateMainRelsXML());
  
  // 添加ppt目录结构
  const pptFolder = zip.folder('ppt');
  pptFolder.file('presentation.xml', generatePresentationXML(pages.length));
  
  const pptRelsFolder = pptFolder.folder('_rels');
  pptRelsFolder.file('presentation.xml.rels', generatePresentationRelsXML(pages.length));
  
  // 添加幻灯片
  const slidesFolder = pptFolder.folder('slides');
  const slideRelsFolder = slidesFolder.folder('_rels');
  const mediaFolder = pptFolder.folder('media');
  
  for (let i = 0; i < pages.length; i++) {
    const page = pages[i];
    const slideIndex = i + 1;
    
    // 将Canvas转换为图片数据
    const imageBlob = await canvasToJPG(page.canvas, 90);
    const imageArrayBuffer = await imageBlob.arrayBuffer();
    
    // 添加图片到media文件夹
    const imageName = `image${slideIndex}.jpg`;
    mediaFolder.file(imageName, imageArrayBuffer);
    
    // 创建幻灯片XML
    slidesFolder.file(`slide${slideIndex}.xml`, generateSlideXML(slideIndex, page.width, page.height));
    slideRelsFolder.file(`slide${slideIndex}.xml.rels`, generateSlideRelsXML(slideIndex));
  }
  
  // 添加主题和样式
  const themeFolder = pptFolder.folder('theme');
  themeFolder.file('theme1.xml', generateThemeXML());
}

/**
 * 生成Content Types XML
 */
function generateContentTypesXML(slideCount: number): string {
  const slideOverrides = Array.from({length: slideCount}, (_, i) => 
    `  <Override PartName="/ppt/slides/slide${i + 1}.xml" ContentType="application/vnd.openxmlformats-presentationml.slide+xml"/>`
  ).join('\n');

  return `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Types xmlns="http://schemas.openxmlformats.org/package/2006/content-types">
  <Default Extension="rels" ContentType="application/vnd.openxmlformats-package.relationships+xml"/>
  <Default Extension="xml" ContentType="application/xml"/>
  <Default Extension="jpg" ContentType="image/jpeg"/>
  <Override PartName="/ppt/presentation.xml" ContentType="application/vnd.openxmlformats-presentationml.presentation.main+xml"/>
  <Override PartName="/ppt/theme/theme1.xml" ContentType="application/vnd.openxmlformats-officedocument.theme+xml"/>
${slideOverrides}
</Types>`;
}

/**
 * 生成主要关系XML
 */
function generateMainRelsXML(): string {
  return `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">
  <Relationship Id="rId1" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/officeDocument" Target="ppt/presentation.xml"/>
</Relationships>`;
}

/**
 * 生成演示文稿XML
 */
function generatePresentationXML(slideCount: number): string {
  const slideIds = Array.from({length: slideCount}, (_, i) => 
    `    <p:sldId id="${256 + i}" r:id="rId${i + 2}"/>`
  ).join('\n');
  
  return `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<p:presentation xmlns:a="http://schemas.openxmlformats.org/drawingml/2006/main" xmlns:r="http://schemas.openxmlformats.org/officeDocument/2006/relationships" xmlns:p="http://schemas.openxmlformats.org/presentationml/2006/main">
  <p:sldMasterIdLst>
    <p:sldMasterId id="2147483648" r:id="rId1"/>
  </p:sldMasterIdLst>
  <p:sldIdLst>
${slideIds}
  </p:sldIdLst>
  <p:sldSz cx="9144000" cy="6858000" type="screen4x3"/>
  <p:notesSz cx="6858000" cy="9144000"/>
</p:presentation>`;
}

/**
 * 生成演示文稿关系XML
 */
function generatePresentationRelsXML(slideCount: number): string {
  const slideRels = Array.from({length: slideCount}, (_, i) => 
    `  <Relationship Id="rId${i + 2}" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/slide" Target="slides/slide${i + 1}.xml"/>`
  ).join('\n');
  
  return `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">
  <Relationship Id="rId1" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/theme" Target="theme/theme1.xml"/>
${slideRels}
</Relationships>`;
}

/**
 * 生成幻灯片XML
 */
function generateSlideXML(slideIndex: number, width: number, height: number): string {
  // 计算图片在幻灯片中的位置和大小
  const slideWidth = 9144000; // EMU单位 (1英寸 = 914400 EMU)
  const slideHeight = 6858000;
  
  // 保持图片比例，适应幻灯片大小
  const imageRatio = width / height;
  const slideRatio = slideWidth / slideHeight;
  
  let imageWidth, imageHeight, x, y;
  
  if (imageRatio > slideRatio) {
    // 图片更宽，以宽度为准
    imageWidth = slideWidth * 0.9; // 留10%边距
    imageHeight = imageWidth / imageRatio;
    x = slideWidth * 0.05;
    y = (slideHeight - imageHeight) / 2;
  } else {
    // 图片更高，以高度为准
    imageHeight = slideHeight * 0.9;
    imageWidth = imageHeight * imageRatio;
    x = (slideWidth - imageWidth) / 2;
    y = slideHeight * 0.05;
  }
  
  return `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<p:sld xmlns:a="http://schemas.openxmlformats.org/drawingml/2006/main" xmlns:r="http://schemas.openxmlformats.org/officeDocument/2006/relationships" xmlns:p="http://schemas.openxmlformats.org/presentationml/2006/main">
  <p:cSld>
    <p:spTree>
      <p:nvGrpSpPr>
        <p:cNvPr id="1" name=""/>
        <p:cNvGrpSpPr/>
        <p:nvPr/>
      </p:nvGrpSpPr>
      <p:grpSpPr>
        <a:xfrm>
          <a:off x="0" y="0"/>
          <a:ext cx="0" cy="0"/>
          <a:chOff x="0" y="0"/>
          <a:chExt cx="0" cy="0"/>
        </a:xfrm>
      </p:grpSpPr>
      <p:pic>
        <p:nvPicPr>
          <p:cNvPr id="2" name="PDF Page ${slideIndex}"/>
          <p:cNvPicPr/>
          <p:nvPr/>
        </p:nvPicPr>
        <p:blipFill>
          <a:blip r:embed="rId1"/>
          <a:stretch>
            <a:fillRect/>
          </a:stretch>
        </p:blipFill>
        <p:spPr>
          <a:xfrm>
            <a:off x="${Math.round(x)}" y="${Math.round(y)}"/>
            <a:ext cx="${Math.round(imageWidth)}" cy="${Math.round(imageHeight)}"/>
          </a:xfrm>
          <a:prstGeom prst="rect">
            <a:avLst/>
          </a:prstGeom>
        </p:spPr>
      </p:pic>
    </p:spTree>
  </p:cSld>
  <p:clrMapOvr>
    <a:masterClrMapping/>
  </p:clrMapOvr>
</p:sld>`;
}

/**
 * 生成幻灯片关系XML
 */
function generateSlideRelsXML(slideIndex: number): string {
  return `<?xml version="1.0" encoding="UTF-8" standalone="yes"?>
<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">
  <Relationship Id="rId1" Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/image" Target="../media/image${slideIndex}.jpg"/>
</Relationships>`;
}

